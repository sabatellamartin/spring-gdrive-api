package tecnoinf.rias.entities;

public class Folder {

	private String id;

	private String name;
	
	public Folder() {}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
